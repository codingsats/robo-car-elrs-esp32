#ifndef PINS_H
#define PINS_H

#include <Arduino.h>

namespace Pins {
  // ---------------------------------------------------------------------------
  // RC / ELRS receiver
  // ---------------------------------------------------------------------------

  // ELRS receiver TX -> ESP32 RX pin.
  constexpr uint8_t kRcRx = 26;

  // Not used right now.
  constexpr int8_t kRcTx = -1;

  // ---------------------------------------------------------------------------
  // Battery monitor
  // ---------------------------------------------------------------------------

  // Battery divider midpoint -> ESP32 ADC input.
  // Note: GPIO27 is ADC2 on classic ESP32.
  constexpr uint8_t kBatteryAdc = 27;

  // ---------------------------------------------------------------------------
  // LEDs
  // ---------------------------------------------------------------------------

  constexpr uint8_t kLeftLed = 12;
  constexpr uint8_t kRightLed = 2;

  // ---------------------------------------------------------------------------
  // Buzzer
  // ---------------------------------------------------------------------------

  constexpr uint8_t kBuzzer = 33;

  // ---------------------------------------------------------------------------
  // Ultrasonic sensor
  // ---------------------------------------------------------------------------

  constexpr uint8_t kUltrasonicTrig = 13;
  constexpr uint8_t kUltrasonicEcho = 14;

  // ---------------------------------------------------------------------------
  // Servo
  // ---------------------------------------------------------------------------

  constexpr uint8_t kServoPan = 25;

  // ---------------------------------------------------------------------------
  // IR receiver
  // ---------------------------------------------------------------------------

  constexpr uint8_t kIrReceiver = 4;

  // ---------------------------------------------------------------------------
  // Line tracking sensors
  // ---------------------------------------------------------------------------

  constexpr uint8_t kTrackingLeft = 35;
  constexpr uint8_t kTrackingCenter = 36;
  constexpr uint8_t kTrackingRight = 39;
}

#endif